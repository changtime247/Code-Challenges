/*
Given an array of numbers and also a target number
Only one valid pair of numbers from the array will add up to the argument number.
Return an array, of length two, with the values of the indices at which are the two numbers that add up to the argument number.
Example:
twoSum([0,1,2,3,4,5,6], 11), [5,6]
Iterate through the array within an iteration to find a pair of numbers that add up to the target number
Note that the number cannot equal itself
*/

/**
 * @param {number[]} nums
 * @param {number} target
 * @return {number[]}
 */
var twoSum = function (nums, target) {
  for (let i = 0; i < nums.length; i++) {
    for (let j = 0; j < nums.length; j++) {
      if (i !== j && nums[i] + nums[j] === target) return [i, j]
    }
  }
}
