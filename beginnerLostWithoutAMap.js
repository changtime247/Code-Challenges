// Given an array of integers, return a new array with each value doubled.

// For example:
// [1, 2, 3] --> [2, 4, 6]

// using map workings best to iterate through each element and return a modified array with the same amount of elements as it started with

function maps(x){
    return x.map(ele=>ele*2)
}